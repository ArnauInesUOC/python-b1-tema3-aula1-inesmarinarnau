def add(num_1: float, num_2: float) -> float:
    result = 0
    result = num_1 + num_2
    return result


def subtract(num_1: float, num_2: float) -> float:
    result = 0
    result = num_1 - num_2
    return result
  

def multiply(num_1: float, num_2: float) -> float:
    result = 0
    result = num_1 * num_2
    return result

def divide(num_1: float, num_2: float) -> float:
    result = 0
    result = num_1 / num_2
    return result 